def Find(text):
    """يستخرج الروابط من النص"""
    m = r"(?i)\b((?:https?://|www\d{0,3}[.]|[a-z0-9.\-]+[.][a-z]{2,4}/)(?:[^\s()<>]+|\(([^\s()<>]+|(\([^\s()<>]+\)))*\))+(?:\(([^\s()<>]+|(\([^\s()<>]+\)))*\)|[^\s!()\[\]{};:'\".,<>?«»""'']))"
    url = re.findall(m, text)
    return [x[0] for x in url]


def contains_swear(text: str) -> bool:
    """يتحقق إذا كان النص يحتوي على كلمات من list_UwU"""
    for word in list_UwU:
        if text == word or f" {word} " in text or word in text:
            return True
    return False


